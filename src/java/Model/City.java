public class City {
    private int ID;
    private String name;

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
    public City getCity(){
        CityDAO db = new CityDAO();
        return db.getCity(this.ID);
    }
    
}
